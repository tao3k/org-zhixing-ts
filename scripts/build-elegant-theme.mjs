const capsizeRootVariables = () => {
  const body = createStyleObject({
    fontSize: 16,
    leading: 28,
    fontMetrics: entireMetricsCollection.inter,
  });
  const heading = createStyleObject({
    fontSize: 32,
    leading: 38,
    fontMetrics: entireMetricsCollection.inter,
  });
  const label = createStyleObject({
    fontSize: 11,
    leading: 15,
    fontMetrics: entireMetricsCollection.robotoMono,
  });
  return [
    `  --type-body-trim-before: ${body["::before"].marginBottom};`,
    `  --type-body-trim-after: ${body["::after"].marginTop};`,
    `  --type-heading-trim-before: ${heading["::before"].marginBottom};`,
    `  --type-heading-trim-after: ${heading["::after"].marginTop};`,
    `  --type-label-trim-before: ${label["::before"].marginBottom};`,
    `  --type-label-trim-after: ${label["::after"].marginTop};`,
  ].join("\n");
};

await mkdir(dirname(generatedVarsPath), { recursive: true });

const styleDictionary = new StyleDictionary({
  source: ["tokens/elegant.json"],
  platforms: {
    css: {
      transformGroup: "css",
      buildPath: ".cache/org-zhixing/",
      files: [
        {
          destination: "elegant-theme-vars.css",
          format: "css/variables",
          options: { showFileHeader: false },
        },
      ],
    },
  },
});
await styleDictionary.buildAllPlatforms();

const generatedVars = await readFile(generatedVarsPath, "utf8");
const themeCss = [
  "/* Generated by scripts/build-elegant-theme.mjs from tokens/elegant.json. */",
  "/* Do not edit src/styles/theme.css directly. */",
  ...radixImports,
  "",
  fontFaces,
  "",
  generatedVars.replace(/\n}\n?$/, `\n${capsizeRootVariables()}\n}\n`),
].join("\n");

await writeFile(themePath, themeCss, "utf8");
